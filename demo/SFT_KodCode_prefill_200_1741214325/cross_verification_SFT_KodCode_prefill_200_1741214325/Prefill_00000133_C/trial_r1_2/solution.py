def multiply_by_three(number):
    """Multiply a number by 3 and return the result.
    
    Args:
        number (int or float): The number to be multiplied by 3.
    
    Returns:
        int or float: The result of the multiplication.
    
    Examples:
        >>> multiply_by_three(2)
        6
        >>> multiply_by_three(0)
        0
        >>> multiply_by_three(-1)
        -3
        >>> multiply_by_three(100)
        300
    """
    return number * 3